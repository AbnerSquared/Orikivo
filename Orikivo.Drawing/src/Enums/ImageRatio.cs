namespace Orikivo.Drawing
{
    // TODO: Move to Arcadia.Graphics
    /// <summary>
    /// Defines common ratios for an image.
    /// </summary>
    public enum ImageRatio
    {
        /// <summary>
        /// Defines a ratio of 16:9.
        /// </summary>
        Widescreen = 1,

        /// <summary>
        /// Defines a ratio of 2:1.
        /// </summary>
        Wide = 2,

        /// <summary>
        /// Defines a ratio of 4:3.
        /// </summary>
        Rectangle = 3,

        /// <summary>
        /// Defines a ratio of 1:1.
        /// </summary>
        Square = 4,

        /// <summary>
        /// Defines a ratio of 1:2.
        /// </summary>
        Tall = 5
    }
}
