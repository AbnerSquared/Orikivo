namespace Orikivo.Desync
{
    /// <summary>
    /// Defines a specific biome type for a <see cref="Biome"/>.
    /// </summary>
    public enum BiomeType
    {
        /// <summary>
        /// Defines the <see cref="Biome"/> as grasslands.
        /// </summary>
        Grasslands = 1,

        /// <summary>
        /// Defines the <see cref="Biome"/> as snowy.
        /// </summary>
        Snow = 2,

        /// <summary>
        /// Defines the <see cref="Biome"/> as lifeless.
        /// </summary>
        Deprived = 4
    }
}
