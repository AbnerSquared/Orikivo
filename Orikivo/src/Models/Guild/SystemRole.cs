namespace Orikivo
{
    /// <summary>
    /// Defines a collection of system roles.
    /// </summary>
    public enum SystemRole
    {
        /// <summary>
        /// Represents the default role that will be given to a user upon joining a guild.
        /// </summary>
        Default = 1,

        /// <summary>
        /// Represents the role that will allow users to execute moderator commands.
        /// </summary>
        Trusted = 2
    }
}
